def solve_quadratic():
    """
    –ù–∞–ø–∏—à—ñ—Ç—å –ø—Ä–æ–≥—Ä–∞–º—É, —è–∫–∞ —Ä–æ–∑–≤'—è–∑—É—î –∫–≤–∞–¥—Ä–∞—Ç–Ω–µ —Ä—ñ–≤–Ω—è–Ω–Ω—è ùëéùë•¬≤ + ùëèùë• + ùëê = 0 —É –¥—ñ–π—Å–Ω–∏—Ö —á–∏—Å–ª–∞—Ö. –ù–∞ –≤—ñ–¥–º—ñ–Ω—É –≤—ñ–¥ –∞–Ω–∞–ª–æ–≥—ñ—á–Ω–æ—ó –≤–ø—Ä–∞–≤–∏ –∑ –º–∏–Ω—É–ª–æ–≥–æ —É—Ä–æ–∫—É, –ø—Ä–æ–≥—Ä–∞–º–∞ –ø–æ–≤–∏–Ω–Ω–∞ –≤–∏–¥–∞–≤–∞—Ç–∏ –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è –ø—Ä–æ –≤—ñ–¥—Å—É—Ç–Ω—ñ—Å—Ç—å –¥—ñ–π—Å–Ω–∏—Ö –∫–æ—Ä–µ–Ω—ñ–≤, —è–∫—â–æ –∑–Ω–∞—á–µ–Ω–Ω—è –¥–∏—Å–∫—Ä–∏–º—ñ–Ω–∞–Ω—Ç–∞ ùê∑ = ùëè¬≤ ‚àí 4ùëéùëê –Ω–µ–≥–∞—Ç–∏–≤–Ω–µ, —î–¥–∏–Ω–µ —Ä—ñ—à–µ–Ω–Ω—è ùë• = ‚àíùëè / 2ùëé, —è–∫—â–æ –≤—ñ–Ω –¥–æ—Ä—ñ–≤–Ω—é—î –Ω—É–ª—é, –∞–±–æ –¥–≤–∞ –∫–æ—Ä–µ–Ω—ñ ùë•‚ÇÅ,‚ÇÇ = (‚àíb¬±‚àöD) / 2a, —è–∫—â–æ –≤—ñ–Ω –ø–æ–∑–∏—Ç–∏–≤–Ω–∏–π.
    """
    a = float(input("–í–≤–µ–¥—ñ—Ç—å –∫–æ–µ—Ñ—ñ—Ü—ñ—î–Ω—Ç a: "))
    b = float(input("–í–≤–µ–¥—ñ—Ç—å –∫–æ–µ—Ñ—ñ—Ü—ñ—î–Ω—Ç b: "))
    c = float(input("–í–≤–µ–¥—ñ—Ç—å –∫–æ–µ—Ñ—ñ—Ü—ñ—î–Ω—Ç c: "))

    D = b**2 - 4*a*c

    if D < 0:
        print("–†—ñ–≤–Ω—è–Ω–Ω—è –Ω–µ –º–∞—î –¥—ñ–π—Å–Ω–∏—Ö –∫–æ—Ä–µ–Ω—ñ–≤.")
    elif D == 0:
        x = -b / (2*a)
        print(f"–†—ñ–≤–Ω—è–Ω–Ω—è –º–∞—î –æ–¥–∏–Ω –∫–æ—Ä—ñ–Ω—å: x = {x}")
    else:
        x1 = (-b + math.sqrt(D)) / (2*a)
        x2 = (-b - math.sqrt(D)) / (2*a)
        print(f"–†—ñ–≤–Ω—è–Ω–Ω—è –º–∞—î –¥–≤–∞ –∫–æ—Ä–µ–Ω—ñ: x‚ÇÅ = {x1}, x‚ÇÇ = {x2}")

# –ó–∞–≤–¥–∞–Ω–Ω—è 4
def calculator():
    """
    –ù–∞–ø–∏—à—ñ—Ç—å –ø—Ä–æ–≥—Ä–∞–º—É-–∫–∞–ª—å–∫—É–ª—è—Ç–æ—Ä, –≤ —è–∫—ñ–π –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á –∑–º–æ–∂–µ –æ–±—Ä–∞—Ç–∏ –æ–ø–µ—Ä–∞—Ü—ñ—é, –≤–≤–µ—Å—Ç–∏ –Ω–µ–æ–±—Ö—ñ–¥–Ω—ñ —á–∏—Å–ª–∞ —Ç–∞ –æ—Ç—Ä–∏–º–∞—Ç–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç. –û–ø–µ—Ä–∞—Ü—ñ—ó, —è–∫—ñ –Ω–µ–æ–±—Ö—ñ–¥–Ω–æ —Ä–µ–∞–ª—ñ–∑—É–≤–∞—Ç–∏: –¥–æ–¥–∞–≤–∞–Ω–Ω—è, –≤—ñ–¥–Ω—ñ–º–∞–Ω–Ω—è, –º–Ω–æ–∂–µ–Ω–Ω—è, –¥—ñ–ª–µ–Ω–Ω—è, –∑–≤–µ–¥–µ–Ω–Ω—è –≤ —Å—Ç—É–ø—ñ–Ω—å, –∫–≤–∞–¥—Ä–∞—Ç–Ω–∏–π –∫–æ—Ä—ñ–Ω—å, –∫—É–±—ñ—á–Ω–∏–π –∫–æ—Ä—ñ–Ω—å, —Å–∏–Ω—É—Å, –∫–æ—Å–∏–Ω—É—Å —Ç–∞ —Ç–∞–Ω–≥–µ–Ω—Å —á–∏—Å–ª–∞.
    """
    operation = input("–û–±–µ—Ä—ñ—Ç—å –æ–ø–µ—Ä–∞—Ü—ñ—é (+, -, *, /, ^, sqrt, cbrt, sin, cos, tan): ")
    if operation in ['+', '-', '*', '/', '^']:
        num1 = float(input("–í–≤–µ–¥—ñ—Ç—å –ø–µ—Ä—à–µ —á–∏—Å–ª–æ: "))
        num2 = float(input("–í–≤–µ–¥—ñ—Ç—å –¥—Ä—É–≥–µ —á–∏—Å–ª–æ: "))

        if operation == '+':
            print(f"–†–µ–∑—É–ª—å—Ç–∞—Ç: {num1 + num2}")
        elif operation == '-':
            print(f"–†–µ–∑—É–ª—å—Ç–∞—Ç: {num1 - num2}")
        elif operation == '*':
            print(f"–†–µ–∑—É–ª—å—Ç–∞—Ç: {num1 * num2}")
        elif operation == '/':
            if num2 != 0:
                print(f"–†–µ–∑—É–ª—å—Ç–∞—Ç: {num1 / num2}")
            else:
                print("–î—ñ–ª–µ–Ω–Ω—è –Ω–∞ –Ω—É–ª—å –Ω–µ–º–æ–∂–ª–∏–≤–µ")
        elif operation == '^':
            print(f"–†–µ–∑—É–ª—å—Ç–∞—Ç: {num1 ** num2}")

    elif operation in ['sqrt', 'cbrt', 'sin', 'cos', 'tan']:
        num = float(input("–í–≤–µ–¥—ñ—Ç—å —á–∏—Å–ª–æ: "))

        if operation == 'sqrt':
            if num >= 0:
                print(f"–†–µ–∑—É–ª—å—Ç–∞—Ç: {math.sqrt(num)}")
            else:
                print("–ö–≤–∞–¥—Ä–∞—Ç–Ω–∏–π –∫–æ—Ä—ñ–Ω—å –∑ –≤—ñ–¥'—î–º–Ω–æ–≥–æ —á–∏—Å–ª–∞ –Ω–µ–º–æ–∂–ª–∏–≤–∏–π")
        elif operation == 'cbrt':
            print(f"–†–µ–∑—É–ª—å—Ç–∞—Ç: {num ** (1/3)}")
        elif operation == 'sin':
            print(f"–†–µ–∑—É–ª—å—Ç–∞—Ç: {math.sin(num)}")
        elif operation == 'cos':
            print(f"–†–µ–∑—É–ª—å—Ç–∞—Ç: {math.cos(num)}")
        elif operation == 'tan':
            print(f"–†–µ–∑—É–ª—å—Ç–∞—Ç: {math.tan(num)}")
    else:
        print("–ù–µ–≤—ñ–¥–æ–º–∞ –æ–ø–µ—Ä–∞—Ü—ñ—è")

